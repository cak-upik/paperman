    public komposisiSetoran showDialog(komposisiSetoran ks) {
        this.komposisi = ks;
        txtNamaKomposisi.setText(komposisi.getNamaKomposisi());
        txtAngsuran.setText(TextComponentUtils.formatNumber(komposisi.getAngsuran()));
        txtTabungan.setText(TextComponentUtils.formatNumber(komposisi.getTabungan()));
        if (komposisi.getStatus().compareTo(Boolean.TRUE) == 0) {
            cbDefault.setSelected(true);
        } else {
            cbDefault.setSelected(false);
        }
        listKomposisi.add(ks);
        this.setVisible(true);
        return komposisi;
    }

