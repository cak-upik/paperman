    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        if (komposisi == null) {
            komposisi = new komposisiSetoran();
        }
        if (!txtNamaKomposisi.getText().isEmpty() && !txtAngsuran.getText().isEmpty() && !txtTabungan.getText().isEmpty()) {
            if (Sistem.isEditOrDeleteFire) {
                komposisi.setId(listKomposisi.get(0).getId());
            }
            komposisi.setNamaKomposisi(txtNamaKomposisi.getText());
            komposisi.setAngsuran(TextComponentUtils.parseNumberToBigDecimal(txtAngsuran.getText()));
            komposisi.setTabungan(TextComponentUtils.parseNumberToBigDecimal(txtTabungan.getText()));
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Tambah Komposisi Gagal\nPeriksa Lagi", "Pesan Sistem", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnTambahActionPerformed

