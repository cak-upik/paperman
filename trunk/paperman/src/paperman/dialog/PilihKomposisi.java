    private void tblKomposisiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKomposisiMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            komposisi = new komposisiSetoran();
            komposisi = listKomposisi.get(tblKomposisi.getSelectedRow());
            this.dispose();
        }
    }//GEN-LAST:event_tblKomposisiMouseClicked

    private void cbSetDefaultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbSetDefaultActionPerformed
        // TODO add your handling code here:
        if (cbSetDefault.isSelected()) {
            cbSetDefault.setFont(new Font("BPG Chveulebrivi", Font.BOLD, 13));
            cbSetDefault.setText("Selalu Ingat Pilihan Ini");
            int pil = JOptionPane.showConfirmDialog(this, "Anda Yakin Akan Menjadikan Komposisi " + komposisi.getNamaKomposisi() + "Sebagai Default ?", "Pesan Sistem", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (pil == JOptionPane.YES_OPTION) {
                komposisi.setStatus(true);
                Main.getSistemService().save(komposisi);
                this.dispose();
            }
            if (pil == JOptionPane.NO_OPTION) {
                cbSetDefault.setSelected(false);
                cbSetDefault.setFont(new Font("BPG Chveulebrivi", Font.PLAIN, 13));
                cbSetDefault.setText("Selalu Ingat Pilihan Ini");
                return;
            }
        } 
    }//GEN-LAST:event_cbSetDefaultActionPerformed
