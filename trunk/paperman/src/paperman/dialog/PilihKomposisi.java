    private void LoadDataToTable() {
        listKomposisi = Main.getSistemService().komposisiRecord();
        tblKomposisi.setModel(new PilihKomposisiSetoranTableModel(listKomposisi));
        initColumnSize();
    }

    private void LoadDataBonusToTable() {
        listKomposisi = Main.getSistemService().findBonusBulanan();
        tblKomposisi.setModel(new PilihKomposisiSetoranTableModel(listKomposisi));
        initColumnSize();
    }

    private void LoadDataBonusPutihToTable() {
        komposisi = Main.getSistemService().findBonusBulananPutih();
        listKomposisi.add(komposisi);
        tblKomposisi.setModel(new PilihKomposisiSetoranTableModel(listKomposisi));
        initColumnSize();
    }

    private void initColumnSize() {
        tblKomposisi.getColumnModel().getColumn(0).setMinWidth(150);
        tblKomposisi.getColumnModel().getColumn(0).setMaxWidth(200);
        tblKomposisi.getColumnModel().getColumn(1).setMinWidth(150);
        tblKomposisi.getColumnModel().getColumn(1).setMaxWidth(200);
        tblKomposisi.getColumnModel().getColumn(2).setMinWidth(150);
        tblKomposisi.getColumnModel().getColumn(2).setMaxWidth(200);
    }

    public void valueChanged(ListSelectionEvent e) {
        if (tblKomposisi.getSelectedRow() >= 0) {
            komposisi = listKomposisi.get(tblKomposisi.getSelectedRow());
        }
    }

